			<h2>Que son las tecnopoeticas</h2>
					<p>
						Las tecnopoeticas consisten en un conjunto de disciplinas artísticas
						que se caracterizan por hacer uso de la tecnologia como herramienta.
						<br />
						Dentro de las tecnopoeticas podemos encontrar diversos tipos de arte
						y entrecruzamientos de disciplinas que van desde un uso de la
						tecnologia como soporte material como el caso del NETArt, o tambien
						vinculados a la ciencia como el caso del Bioarte, o entrecruzando
						disciplinas como el caso de las video-instalaciones, asi como
						tambien los cruces entre lenguajes artisticos como es el caso de la
						poesia digital, la video-danza, musica electronica y diversos tipos
						de formas experimentales de producir signifiado a través de la
						tecnología.
					</p>
			<h3>Historia</h3>
					<p>
						Como periodos historicos significativos de producción y desarrollo
						tecnopoético podemos descatar la decada del 1920 en la
						experimentacion vanguardia fue un punto inicial y pionero para las
						formas experimentales de hacer arte.
						<br />
						Luego el periodo de los 1960 con la irrupción del arte
						contemporáneo, las formas de hacer y significar ene l arte se ven
						alteradas y revolucionadas.
						<br />
						Es asi como surgen obras que comienzan a problematizar la tecnologia
						y/o haciendo uso de ella. Como ejemplo de este periodo y pionera de
						tecnopoetica asi como tambien de arte contemporaneo argentino puede
						tomarse la obra Minuphone de Martha Minujin (1967)
						<br />
						Posteriormente la decada de los 1990 con la aparicion de internet
						como medio de comunicacion probocando una nuevo medio de
						comunicacion mas cotidiano y estrechamente vinculado al uso de
						dispositivos.
						<br />
						Esto provocó que el arte problematice la tecnologia desde un nuevo
						enfoque. <br />
						Asi mismo durante este periodo la forma de acceder a distintos y
						nuevos dispositivos tecnológicos posibilitando la experimentación.
					</p>
